# variable assignment equal to 10
types_of_people = 10
# variable assignment equal to whats in the quotes
x = "There are #{types_of_people} types of people."
# variable assignment of whats in the quotes
binary = "binary"
# variable assignment of whats in the quotes
# can't use single quotes with this one
do_not = "don't"
# variable assignment with interpolation.
y = "Those who know #{binary} and those who #{do_not}."

# prints what x and y are equal too
puts x
puts y

# prints the string + interpolation
puts "I said: #{x}."
puts "I also said: '#{y}'."

# boolean variable assignment
hilarious = false

# variable assignment
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# prints what joke_evaluation is equal too
puts joke_evaluation

