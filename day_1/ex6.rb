types_of_people = 10

# sets the variable x equal to the string
x = "There are #{types_of_people} types of people."

# set the variable binary to the string
binary = "binary"

# set the variable do_not to the string
do_not = "don't"

# set the variable y to the string
y = "Those who know #{binary} and those who #{do_not}."

# prints out the strings that the variables represent
puts x
