# declares variable
types_of_people = 10
# declares variable and assigns a string with another variable inside it.
x = "There are #{types_of_people} types of people."
# declares variable
binary = "binary"
# declares variable
do_not = "don't"
# declares variable and uses other variables inside it
y = "Those who know #{binary} and those who #{do_not}."
# prints x variable
puts x
# prints y variable
puts y
# prints a string that includes a variable in the string
puts "I said: #{x}."
# same as before
puts "I also said: '#{y}'."
# declares a variable with a Boolean value
hilarious = false
# declares variable with a variable inside a string.
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"
# prints variable
puts joke_evaluation
# declares a variable
w = "This is the left side of..."
# declares variable
e = "a string with a right side."
# prints variables together
puts w + e

