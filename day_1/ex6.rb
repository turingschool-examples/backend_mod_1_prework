types_of_people = 10
# x is a string that contains the types_of_people variable
x = "There are #{types_of_people} types of people."
# binary is a string of "binary"
binary = "binary"
# do_not is a string of "don't"
do_not = "don't"
# y is a string that contains the binary and do_not variable
y = "Those who know #{binary} and those who #{do_not}."

# Outputs value of x
puts x
# Outputs value of y
puts y

# Outputs string and value of x
puts "I said: #{x}."
