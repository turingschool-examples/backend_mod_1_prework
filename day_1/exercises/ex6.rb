# variable assignment of integer value
types_of_people = 10

# variable assignment with variable evaluation inside the string
x = "There are #{types_of_people} types of people."

# variable assignment string value
binary = "binary"

# variable assignment string value
do_not = "don't"

# variable assignment with variable evaluation inside the string
y = "Those who know #{binary} and those who #{do_not}."

# Printing variables
puts x
puts y

# printing string with variable evaluation inside the string
puts "I said: #{x}"
puts "I also said: '#{y}'."

# variable assignment boolean value
hilarious = false

# variable assignment with variable evaluation inside the string
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# printing variable
puts joke_evaluation

# variable assignment string values
w = "This is the left side of..."
e = "a string with a right side."

# printing expression evaluation (string concatenation)
puts w + e

