# + plus means addition
# - minus means subtraction
# / slash means devision
# * asterisk means multiplication
# % percent means remainder after devision
# < less-than means less-than
# > greater-than means greater-than
# <= less-than-equal means less than or equal to
# >= greater-than-equal means greater than or equal to

# prints a string
puts "I will now count my chickens:"
# prints a string with a computation in the middle
puts "Hens #{25.0 + 30.0 / 6.0}"
# prings a string with a computation in the middle
puts "Roosters #{100.0 - 25.0 * 3.0 % 4.0}"
# prints a string
puts "Now I will count the eggs:"
# prints the answer to the computation
puts 3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 - 1.0 / 4.0 + 6.0
# prints a string. does not do computation
puts "Is it true that 3 + 2 < 5 - 7?"
# prints answer to computation
puts 3.0 + 2.0 < 5.0 - 7.0
# prints string with answer to computation in middle
puts "what is 3 + 2? #{3.0 + 2.0}"
# prints string with answer to computation in middle
puts "What is 5 - 7? #{5.0 - 7.0}"
# prints string
puts "Oh, that's why it's false."
# prints string with computation in the middle
puts "Is it greater? #{5.0 > -2.0}"
# prints string with computation in the middle
puts "Is it greater or equal? #{5.0 >= -2.0}"
# prints string with computation in the middle
puts "Is it less or equal? #{5.0 <= -2.0}"
# prints answer to computation
