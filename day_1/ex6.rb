# Strings and Text

# this variable is represented with a binary number that represents 2
types_of_people = 10
# x is a variable that displays a string involving the types_of_people variable
x = 'There are #{types_of_people} types of people.'
# binary is a variable that displays as "binary"
binary = 'binary'
# do_not is a variable that displays as "don't"
do_not = 'don"t'
# y is a variable that displays a string involving the binary variable and the do_not variable
y = 'Those who know #{binary} and those who #{do_not}.'

# prints the x variable string
puts x
# prints the y variable string
puts y

# prints a string that includes the x variable
puts 'I said: #{x}.'
# prints a string that includes the y variable
puts 'I also said: "#{y}".'

# the hilarious variable displays as "false"
hilarious = false
# the joke_evaluation variable displays a question and answers it
joke_evaluation = 'Isn"t that joke so funny?! #{hilarious}'

# prints the joke_evaluation variable
puts joke_evaluation

# the w valuable is a string
w = 'This is the left side of...'
# the e variable is a string
e = 'a string with a right side.'

# print the w variable plus the e variable
puts w + e

# Study Drill 4: When adding the w variable string to the e variable string, one
# is combinging the two strings into one longer string.

