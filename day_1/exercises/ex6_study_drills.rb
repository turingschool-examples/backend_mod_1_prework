# variable that = the integer 10
types_of_people = 10
# variable that = string and includes another variable (integer)
x = "There are #{types_of_people} types of people."
# variable that = string
binary = "binary"
# variable that = string
do_not = "don't"
# variable that = string and includes two more variables (both strings)
y = "Those who know #{binary} and those who #{do_not}."

# "prints" the variable x
puts x
# "prints" the variable y
puts y

# "prints" a string with the variable x (string) in it
puts "I said: #{x}."
# "prints" a string with the variable y (string) in it
puts "I also said: '#{y}'."

# creates a variable that = false
hilarious = false
# variable that = string with the variable hilarious (boolean)
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# "prints" the variable joke_evaluation
puts joke_evaluation

# variable that = a string
w = "This is the left side of..."
# variable that = a string
e = "a string with a right side."

# "prints" variable w combined (+) with variable e
puts w + e

# adding w + e makes the string longer because it
# is the combination of two shorter strings.
