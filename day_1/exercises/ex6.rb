# variable assignment
types_of_people = 10
x = "There are #{types_of_people} types of people."
binary = "binary"
do_not = "don't"
y = "Those who know #{binary} and those who #{do_not}."

# prints variables x and y from above
puts x
puts y

# prints strings, including the variables x and y
puts "I said: #{x}."
puts "I also said: '#{y}'."

# additional variable assignment
hilarious = false
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# prints variable above, including the variable hilarious
puts joke_evaluation

# string variable assignment
w = "This is the left side of..."
e = "a string with a right side."

# concatenates variable strings
puts w + e
