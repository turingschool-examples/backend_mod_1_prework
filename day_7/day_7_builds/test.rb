def number_convert(number)

  conversion_table = {
      1 => "one",
      2 => "two",
      3 => "three",
      4 => "four",
      5 => "five",
      6 => "six",
      7 => "seven",
      8 => "eight",
      9 => "nine",
      10 => "ten",
      11 => "eleven",
      12 => "twelve",
      13 => "thirteen",
      14 => "fourteen",
      15 => "fifteen",
      16 => "sixteen",
      17 => "seventeen",
      18 => "eighteen",
      19 => "nineteen",
      20 => "twenty",
      30 => "thirty",
      40 => "forty",
      50 => "fifty",
      60 => "sixty",
      70 => "seventy",
      80 => "eighty",
      90 => "ninety",
      100 => "hundred",
      1000 => "thousand",
      1000000 => "million"
    }
  converted_number = ""
  conversion_table.each do |integer, name|
    if number == 0
      return converted_number
    elsif number.to_s.length == 1 && number / integer > 0
      return converted_number + "#{name}"
    elsif number < 100 && number / integer > 0
      return converted_number + "#{name}" if number % integer == 0
      return converted_number + "#{name}" + number_convert(number % integer)
    elsif number / integer > 0
      return converted_number + number_convert(number / integer) + "#{name}" + number_convert(number % integer)
    end
  end
end

