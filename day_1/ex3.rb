# The following line will print the text between the quotes
puts "I will now count my chickens:"

#The next line will ask ruby to make the calculation and include it in the string when it prints it.
puts "Hens #{25 + 30 / 6}"
#The next like will do the same thing as commented above.
puts "Roosters #{100 - 25 * 3 % 4}"

#This line will print the string or the text between the quotes.
puts "Now I will count the eggs:"

# The next like will perform the calculation and print the result.
puts 3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6

# The next line will print as types because it is a string. No calculation will be perfomed.
puts "Is it true that 3.5 + 2 < 5.8 - 7?"

# This line will perform the calculation and return a true or false value because of the greater than sign.
puts 3.5 + 2 < 5.8 - 7

# The next two lines will print with the result of the calculation as part of the string.
puts "What is 3 + 2? #{3.5 + 2}"
puts "What is 5 - 7? #{5.8 - 7}"

# This line will only print the text between the quotes.
puts "Oh, that's why it's false."

# Same as above with this line.
puts "How about some more."

# These next three will also perform the true or false calculation and print the result as part of the string between the quotes.
puts "Is it greater? #{5 > -2}"
puts "Is it greater or equal? #{5 >= -2}"
puts "Is it less or equal? #{5 <= -2}"
