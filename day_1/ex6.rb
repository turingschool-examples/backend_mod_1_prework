# the variable types_of_people is given the value 10
types_of_people = 10
# the variable x is assigned the string with the variable types of people in it
x = "There are #{types_of_people} types of people."
# the variable binary is assigned the string binary
binary = "binary"
# the variable do_not is assigned the string don't
do_not = "don't"
# the variable y is assigned the string with the variables binary and do_not in it
y = "Those who know #{binary} and those who #{do_not}."

# the string assigned to variable x is printed
puts x
# the string assigned to variable y is printed
puts y
# the string "I said" plus the string assigned to variable x is printed
puts "I said: #{x}."
# the string "I also said" plus the string assigned to variable y is printed
puts "I also said: '#{y}'."

# the variable hilarious is given the value false
hilarious = false
# the variable joke_evaluation is given the string plus the variable hilarious at the end
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# the string assigned to joke_evaluation is printed
puts joke_evaluation

