# setting "types_of_people" equal to 10
types_of_people = 10
# set x equal to a string containing a varible
x = "There are #{types_of_people} types of people."
# setting binary equal to the string "binary"
binary = "binary"
# do_not is now equal to the string "don't"
do_not = "don't"
# y is now equal to a string of text incorporating two variables
y = "Those who know #{binary} and those who #{do_not}."
# printing variables
puts x
puts y
# printing strings embedded with variables
puts "I said: #{x}."
puts "I also said: '#{y}'."
# variables can also equal boolean values
hilarious = false
# joke_evaluation now equals a string of text containing a variable
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"
#print the variable
puts joke_evaluation
# more variables being equated to strings
w = "This is the left side of..."
e = "a string with a right side."
# two variables containing strings can even be combined
puts w + e

