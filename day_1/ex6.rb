# set variable for types of people
types_of_people = 10
# string interpolation of types of people
x = "There are #{types_of_people} types of people."
# set binary variable
binary = "binary"
# set do not variable to "don't"
do_not = "don't"
# string interpolation of those who know binary and those who do not
y = "Those who know #{binary} and those who #{do_not}."

# print x variable
puts x
# print y variable
puts y

# string interpolation "I said x"
puts "I said: #{x}."
# string interpolation "I also said y"
puts "I also said: '#{y}'."

# set hilarious boolean to false
hilarious = false
# set joke evaluation variable with interpolation of hilarious boolean
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# print joke evaluation
puts joke_evaluation

# set w variable as left side of string
w = "This is the left side of..."
# set e variable as right side of string
e = "a string with a right side."
