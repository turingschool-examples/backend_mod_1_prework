#assigns the variable do_not to the string in quotes
do_not = "don't"
#assigns the variable y to the string with embedded formulas
y = "Those who know #{binary} and those who #{do_not}."

#prints what is assigned to variable x
puts x
#prints what is assigned to variable y
puts y

#prints the string with embedded formula
puts "I said: #{x}."
