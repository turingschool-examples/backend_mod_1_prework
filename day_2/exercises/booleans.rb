# Truth tables
# NOT (!)
# !false = true
# !true  = false

# OR (||)
# true || false  = true
# true || true   = true
# false || true  = true
# false || false = false

# AND (&&)
# true && false  = false
# true && true   = true
# false && true  = false
# false && false = false

# NOT OR
# !(true || false)  = false
# !(true || true)   = false
# !(false || true)  = false
# !(false || false) = true

# NOT AND
# !(true && false)  = true
# !(true && true)   = false
# !(false && true)  = true
# !(false && false) = true
