# defines drivers as a variable equal to 30
drivers = 30
# defines passengers as a variable and sets it equal to 90
passengers = 90
# defines the variable cars_not_driven as the result of cars minus drivers
cars_not_driven = cars - drivers
# defines the variable cars_driven as equal to drivers
cars_driven = drivers
# defines carpool_capacity as the result of cars_driven multiplied by space_in_a_car
carpool_capacity = cars_driven * space_in_a_car
# defines average_passengers_per_car as the result of passengers divided by cars_driven
average_passengers_per_car = passengers / cars_driven

# prints the following statement with the variable #{cars} being output inside the string
