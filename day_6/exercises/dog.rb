# the dog being hungry. Call that method below the class, and print the dog's
# hunger status.

class Dog
  attr_reader :breed, :name, :age, :hungry

  def initialize(breed, name, age)
    @breed  = breed
    @name   = name
    @age    = age
    @hungry = true
  end

  def bark
    p "woof!"
  end

  def eat
    @hungry = false
  end

  def play
    @hungry = true
    if @hungry == true
      puts "#{@name} played a game but is now SO hungry. You should feed him!"
    else
      puts "#{@name} has lots of energy!"
    end
  end

end

