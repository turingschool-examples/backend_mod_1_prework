# + does addition
# - does subtraction
# / does division
# * does multiplication
# % shows remainder from an integer division problem
# < shows if "less than"
# > shows if "greater than"
# <= shows if "less than or equal to"
# >= shows if "greater than or equal to"

#prints the text
puts "I will now count my chickens:"

#prints text and completes math within the brackets
puts "Hens #{25.0 + 30.0 / 6.0}"
puts "Roosters #{100.0 - 25.0 * 3.0 % 4.0}"

#prints the text
puts "Now I will count the eggs:"

#completes the math below
puts 3.0 + 2.0 + 1.0 - 5.0 + 4.0 % 2.0 - 1.0 / 4.0 + 6.0

#prints the text
puts "Is it true that 3 + 2 < 5 - 7?"

#completes math and then prints if it is true/false
puts 3.0 + 2.0 < 5.0 - 7.0

#prints text and then completes math within brackets
puts "What is 3 + 2? #{3.0 + 2.0}"
puts "What is 5 - 7? #{5.0 - 7.0}"

#prints the text
puts "Oh, that's why it's false."

puts "How about some more."

#prints the text, completes math within the brackets, then prints if it is true/false
puts "Is it greater? #{5.0 > -2.0}"
puts "Is it greater or equal? #{5.0 >= -2.0}"
puts "Is it less or equal? #{5.0 <= -2.0}"
