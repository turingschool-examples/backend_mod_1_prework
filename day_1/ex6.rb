# creating a variable
types_of_people = 10
# creating a variable consisting of a string with another inline variable
x = "There are #{types_of_people} types of people."
# creating a variable set to a string
binary = "binary"
# creating another variable set to a string
do_not = "don't"
# creating a variable set to a string and two inline variables
y = "Those who know #{binary} and those who #{do_not}."

# putting different created variables
puts x
puts y

# putting strings with inline variables
puts "I said: #{x}."
puts "I also said: '#{y}'."

# creating variable set to a boolean value
hilarious = false
# creating another variable set to a string with an inline variable.
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

# putting a created variable
puts joke_evaluation

# creating two different variables equal to a string
w = "This is the left side of..."
e = "a string with a right side."

# putting two variables consisting of strings side by side
puts w + e

