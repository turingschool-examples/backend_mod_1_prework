def cheese_and_crackers(cheese_count, cracker_boxes)
  puts "You have #{cheese_count} cheese"
  puts "You have #{cracker_boxes} boxes of crackers"
  puts "cool"
  puts "get a picnic blanket\n"
end

#giving functions numbers directly
cheese_and_crackers(20, 30)

#use variables

cheese = 10
crackers = 40

cheese_and_crackers(cheese, crackers)

# math time

cheese_and_crackers(10 + 20, 4 + 7)

# wow real math

cheese_and_crackers(cheese + 5, crackers - 2)
