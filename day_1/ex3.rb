# Displays message within the quotation marks
puts "I will now count my chickens:"

# Displays text within quotations, also the resulting number computed from within the #{}
puts "Hens #{25 + 30 /6}"
# Displays text within quotations, also the computation. Following the rules of math order - 25 * 3 = 75, 75 x .04 = 3, 100 - 3 = 97
puts "Roosters #{100 - 25 * 3 % 4}"

# Displays message within the quotation marks
puts "Now I will count the eggs:"

# Returns answer of math problem, following rules of math order
puts 3 + 2 + 1 - 5 + 4 % 2 - 1 / 4 + 6

# Displays message within the quotation marks
puts "Is it true that 3 + 2 < 5 - 7?"

# Returns boolean value for math problem
puts 3 + 2 < 5 - 7

# Displays message within the quotation marks, along with computed answer
puts "What is 3 + 2? #{3 + 2}"
# Displays message within the quotation marks, along with computed answer
puts "What is 5 - 7? #{5-7}"

# Displays message within the quotation marks
puts "Oh, that's why it's false."

# Displays message within the quotation marks
puts "How about some more."

# Displays message within the quotation marks, along with boolean value
puts "Is it greater? #{5 > -2}"
# Displays message within the quotation marks, along with boolean value
puts "Is it greater or equal? #{5 >= -2}"
# Displays message within the quotation marks, along with boolean value
puts "Is it less or equal #{5 <= -2}"

